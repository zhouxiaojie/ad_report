package com.ocean.ad.report.model;

public class ReportOnDayVo {
	private String fmtDate;
	
	private int succ;
	
	private int fail;
	

	
	public String getFmtDate() {
		return fmtDate;
	}

	public void setFmtDate(String fmtDate) {
		this.fmtDate = fmtDate;
	}

	public int getSucc() {
		return succ;
	}

	public void setSucc(int succ) {
		this.succ = succ;
	}

	public int getFail() {
		return fail;
	}

	public void setFail(int fail) {
		this.fail = fail;
	}

	
	
}
